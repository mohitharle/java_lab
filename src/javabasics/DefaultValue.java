package javabasics;

public class DefaultValue {
	byte b;
	short s;
	int i;
	long l;
	float f;
	double d;
	char c;
	boolean bo;

	public static void main(String[] args) {
     DefaultValue defaultValue=new DefaultValue();
     
     System.out.println(defaultValue.b);
     System.out.println(defaultValue.s);
     System.out.println(defaultValue.i);
     System.out.println(defaultValue.l);
     System.out.println(defaultValue.f);
     System.out.println(defaultValue.d);
     System.out.println(defaultValue.c);
     System.out.println(defaultValue.bo);
	}
}


